bool is_chinese_char(u16 c) {
    // Check if character is in Chinese Unicode range
    return (c >= 0x4E00 && c <= 0x9FFF) || // CJK Unified Ideographs
           (c >= 0x3400 && c <= 0x4DBF) || // CJK Extension A
           (c >= 0x20000 && c <= 0x2A6DF) || // CJK Extension B
           (c >= 0x2A700 && c <= 0x2B73F) || // CJK Extension C
           (c >= 0x2B740 && c <= 0x2B81F) || // CJK Extension D
           (c >= 0x2B820 && c <= 0x2CEAF) || // CJK Extension E
           (c >= 0x2CEB0 && c <= 0x2EBEF);   // CJK Extension F
}

const u8* get_chinese_char_data(u16 c) {
    // Search for character in our font data
    for (int i = 0; i < sizeof(chinese_chars) / sizeof(chinese_chars[0]); i++) {
        if (chinese_chars[i] == c) {
            return &chinese_font_16x16[i * 32]; // 32 bytes per character
        }
    }
    
    // Return default character if not found
    return chinese_font_16x16; // Return first character as fallback
}